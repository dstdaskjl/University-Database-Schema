public class Takes {
    Object ID;
    Object course_id;
    Object sec_id;
    Object semester;
    Object year;
    Object grade;

    public Takes(Object ID, Object course_id, Object sec_id, Object semester, Object year, Object grade){
        this.ID = ID;
        this.course_id = course_id;
        this.sec_id = sec_id;
        this.semester = semester;
        this.year = year;
        this.grade = grade;
    }
}
